package job;

public class jobBean {
	private int num;
	private String name;
	private String admit;
	private String fm;
	private String time;
	private String pay;
	
	private String rename;
	private String reage;
	private String readdr;
	private String relevel;
	private String recar;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAdmit() {
		return admit;
	}
	public void setAdmit(String admit) {
		this.admit = admit;
	}
	public String getFm() {
		return fm;
	}
	public void setFm(String fm) {
		this.fm = fm;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getRename() {
		return rename;
	}
	public void setRename(String rename) {
		this.rename = rename;
	}
	public String getReage() {
		return reage;
	}
	public void setReage(String reage) {
		this.reage = reage;
	}
	public String getReaddr() {
		return readdr;
	}
	public void setReaddr(String readdr) {
		this.readdr = readdr;
	}
	public String getRelevel() {
		return relevel;
	}
	public void setRelevel(String relevel) {
		this.relevel = relevel;
	}
	public String getRecar() {
		return recar;
	}
	public void setRecar(String recar) {
		this.recar = recar;
	}
	
	
	
}
